    if param.title() == "Jabroni":
        return "Patron Tequila"
    if param.title() == "School Counselor":
        return "Anything with Alcohol"
    if param.title() == "Programmer":
        return "Hipster Craft Beer"
    if param.title() == "Bike Gang Member":
        return "Moonshine"
    if param.title() == "Politician":
        return "Your tax dollars"
    if param.title() == "Rapper":
        return "Cristal"
